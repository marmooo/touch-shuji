function getKakusu(object, kanjiId) {
  var id = 'z' + kanjiId + 'd';
  var paths = object.contentDocument.querySelectorAll('[id^="' + id + '"]');
  var ids = [...paths].map(path => path.getAttribute('id'));
  var kakuIds = ids.map(id => {
    if (/[a-z]/.test(id.slice(-1))) {
      return id.slice(0, -1);
    } else {
      return id;
    }
  });
  return uniq(kakuIds).length;
}

function sleep(time) {
  const d1 = new Date();
  while (true) {
    const d2 = new Date();
    if (d2 - d1 > time) {
      return;
    }
  }
}

const alphabets = "abcefghijklmnopqrstuvwxyz".split("");
function getKakuPaths(contentDocument, kanjiId, pos) {
  var id = 'z' + kanjiId + 'd' + pos;
  var paths = contentDocument.querySelectorAll('[id="' + id + '"]');
  if (paths.length != 0) {
    return [...paths];
  } else {
    var paths = [];
    for (var i=0; i<alphabets.length; i++) {
      var path = contentDocument.querySelectorAll('[id="' + id + alphabets[i] + '"]');
      paths.push(...path);
    }
    return paths;
  }
}

function getTehonCanvas(object, kanjiId, kakusu, kakuNo) {
  return new Promise(function(resolve, reject) {
    var clonedContent = object.contentDocument.cloneNode(true);
    var width = 128;
    for (var j=1; j<=kakusu; j++) {
      var kakuPaths = getKakuPaths(clonedContent, kanjiId, j);
      if (kakuNo != j) {
        kakuPaths.forEach(kakuPath => kakuPath.remove());
      } else {
        kakuPaths.forEach(kakuPath => {
          kakuPath.setAttribute('stroke', 'black');
        });
      }
    }
    var text = clonedContent.documentElement.outerHTML;
    var blob = new Blob([text], {type: 'image/svg+xml'});
    var url = URL.createObjectURL(blob);
    var img = new Image();
    img.src = url;
    img.onload = function() {
      var canvas = document.createElement('canvas');
      canvas.width = canvasSize;
      canvas.height = canvasSize;
      var ctx = canvas.getContext('2d');
      ctx.drawImage(img, 0, 0, canvasSize, canvasSize);
      resolve(canvas);
    };
  });
}


